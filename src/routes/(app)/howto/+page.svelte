    <h2>1. Have a goal in mind.</h2>
    <p>
        Consider what you’re trying to learn by conducting this survey.
        Do you have an idea that you want to validate, or are you hoping that you can disprove an assumption under which you have been operating?
        Surveys work best when they focus on one specific goal.
        When building the questionnaire for your survey, it is important to offer questions that support your goal. 
    </p>
    <img class="w-3/5" src="https://resources.pollfish.com/wp-content/uploads/2019/09/Specific-Goals-1.png" alt="">
    <h2>2. Eliminate Jargon.</h2>
    <p>
        Just because a concept is clear to you doesn’t mean your target audience is always on the same page.
        A well-designed questionnaire contains good survey questions to be sure.
        But they also use plain language (no jargon) to explain concepts or acronyms that customers may be unfamiliar with and offer an opt-out for those who are unsure. Don’t be afraid to use more than one question or offer an example to ensure clarity on complex information in your questionnaire template— a confused audience leads to frustration and low-quality responses.
    </p>
    <img class="w-3/5" src="https://resources.pollfish.com/wp-content/uploads/2019/09/Clear-concepts-1.png" alt="">
    <h2>3. Make answer choices clear and distinct.</h2>
    <p>
        When multiple-choice answers are presented, the respondent must make a selection.
        If these responses overlap or are confusing for the respondent, the quality of the data decreases because they aren’t sure what is being asked of them.
        Make sure answers are distinct and specific whenever possible so the respondent can confidently choose the best answer.
    </p>
    <img class="w-3/5" src="https://resources.pollfish.com/wp-content/uploads/2019/09/Clear-answer-choices-1.png" alt="">
    <h2>4. Give users an “other” option.</h2>
    <p>
        Make sure that, in a multiple-choice sequence, you’ve given respondents the chance to opt out of the question if it doesn’t apply to them or if none of their answers fit.
        Provide an option like “no opinion,” “neutral,” or “none of the above.” You can also offer the option to select “other” and provide an open-ended response that can give you more context.
    </p>
    <h2>5. Avoid “yes/ no” screening questions.</h2>
    <p>
        Screening questions help you connect with a qualified audience at the beginning of the survey. When respondents select a qualified response, they will enter the rest of the survey.
        However, people are biased toward choosing “yes” or a positive response when presented with a yes/ no question, even if their real opinion is more neutral.
        To reduce bias, provide a list of answer choices with no indication that one is preferred over the others. 
    </p>
    <img class="w-3/5" src="https://resources.pollfish.com/wp-content/uploads/2019/09/How-to-write-screening-question-1.png" alt="">
    <h2>6. Don’t ask two questions at once.</h2>
    <p>
        Each question should focus on obtaining a specific piece of information.
        When you ask two questions at once using “and” or “or,” you’re introducing another question, which may have a different answer. This will have one of two results: either you’ll confuse your respondents, who are forced to choose the right answer to one question; or your respondents will confuse you with their answers.
        Either way, make sure you write simple survey questions asking for separate pieces of information as separate questions. 
    </p>
    <img class="w-3/5" src="https://resources.pollfish.com/wp-content/uploads/2019/09/One-at-a-Time-1.png" alt="">
    <h2>7. Use different question types.</h2>
    <p>
        Respondents offer better and more thoughtful answers when they are engaged.
        And that means asking several types of research questions. Use ranking, matrix, open-ended, or multiple-choice questions to stimulate them and keep them interested, especially in a longer questionnaire.
        Different question types not only keep the respondents engaged (which can increase your completion rates) but can also elicit different responses.
    </p>
    <h2>8. Always keep the audience in mind.</h2>
    <p>
        Remember as you are writing the questions to always keep your target audience in mind.
        The audience can be as broad as the “general population” or as narrow as you need it to be.
        The important thing is to know who you want to target so you can communicate with them effectively.
    </p>
    <img class="w-3/5" src="https://resources.pollfish.com/wp-content/uploads/2019/09/Target-Audience-1.png" alt="">
    <p>
        Regardless of the types of survey questions you select, questions should be short, clear, and to the point, but also engage the respondent.
        Remember that the less confused your respondents are, the clearer your data will be. 
    </p>
